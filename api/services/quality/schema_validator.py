"""
Schema validator for ReportService outputs.

Ports the rule-based validator from `~/.openclaw/workspace/harness_design/mnc/`
into BDGO so every report generated by `BuyerProfileService` (and future
`ReportService` subclasses) can be checked against a YAML schema contract.

Public API:
    validate_markdown(text, mode="mnc") -> AuditResult
    audit_to_dict(audit, max_findings=100) -> dict

The findings dict is small enough to stash in `report_history.meta_json`
so it travels with every task and surfaces in the UI / queries.

Mode selection:
    "mnc"                 → MNC schema (Pfizer, Sanofi, Lilly tier)
    "commercial_biotech"  → Commercial Biotech buyer schema (Insmed tier)

Schema files live in ``api/services/quality/schemas/``. Adding a new schema
is: drop a YAML next to the existing ones and register its mode in
``_SCHEMA_BY_MODE``.
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

import yaml

_SCHEMAS_DIR = Path(__file__).parent / "schemas"

_SCHEMA_BY_MODE: dict[str, str] = {
    "mnc": "mnc_main.yaml",
    "commercial_biotech": "commercial_biotech_main.yaml",
}


# ────────────────────────────────────────────────
# Data model
# ────────────────────────────────────────────────
@dataclass
class Block:
    """A document block: heading / paragraph / table."""

    kind: str  # 'heading' | 'paragraph' | 'table'
    level: int = 0
    text: str = ""
    n_rows: int = 0
    n_cols: int = 0


@dataclass
class Finding:
    severity: str  # 'fail' | 'warn' | 'info'
    category: str
    section: str
    message: str
    evidence: str = ""


@dataclass
class AuditResult:
    findings: list[Finding] = field(default_factory=list)
    stats: dict[str, Any] = field(default_factory=dict)

    def add(self, severity: str, category: str, section: str, message: str, evidence: str = ""):
        self.findings.append(Finding(severity, category, section, message, evidence[:200]))

    @property
    def n_fail(self) -> int:
        return sum(1 for f in self.findings if f.severity == "fail")

    @property
    def n_warn(self) -> int:
        return sum(1 for f in self.findings if f.severity == "warn")

    @property
    def n_info(self) -> int:
        return sum(1 for f in self.findings if f.severity == "info")


# ────────────────────────────────────────────────
# Document parsing
# ────────────────────────────────────────────────
def load_md_text(text: str) -> list[Block]:
    """Parse markdown string into ordered Block list. # = heading; |…| rows = table."""
    blocks: list[Block] = []
    lines = text.splitlines()
    i = 0
    while i < len(lines):
        line = lines[i].rstrip()
        if not line.strip():
            i += 1
            continue
        m = re.match(r"^(#{1,6})\s+(.+)$", line)
        if m:
            blocks.append(Block(kind="heading", level=len(m.group(1)), text=m.group(2).strip()))
            i += 1
            continue
        if line.strip().startswith("|"):
            tbl_lines = []
            while i < len(lines) and lines[i].strip().startswith("|"):
                tbl_lines.append(lines[i].strip())
                i += 1
            n_rows = sum(1 for line in tbl_lines if not re.match(r"^\|[\s\-:|]+\|$", line))
            n_cols = max((line.count("|") - 1 for line in tbl_lines), default=0)
            blocks.append(
                Block(
                    kind="table",
                    text=" | ".join(tbl_lines),
                    n_rows=n_rows,
                    n_cols=n_cols,
                )
            )
            continue
        para_lines = [line]
        j = i + 1
        while (
            j < len(lines)
            and lines[j].strip()
            and not lines[j].lstrip().startswith("#")
            and not lines[j].strip().startswith("|")
        ):
            para_lines.append(lines[j].rstrip())
            j += 1
        blocks.append(Block(kind="paragraph", text=" ".join(para_lines)))
        i = j
    return blocks


def full_text(blocks: list[Block]) -> str:
    return "\n".join(b.text for b in blocks)


def count_chars(text: str) -> int:
    """Char-level count, whitespace stripped (CJK = 1 char, latin word ≈ N chars)."""
    return len(re.sub(r"\s+", "", text))


# ────────────────────────────────────────────────
# Section splitting
# ────────────────────────────────────────────────
@dataclass
class Section:
    id: str
    name: str
    start_idx: int
    end_idx: int
    blocks: list[Block] = field(default_factory=list)

    @property
    def text(self) -> str:
        return "\n".join(b.text for b in self.blocks)

    @property
    def char_count(self) -> int:
        return count_chars(self.text)

    def contains_heading(self, patterns: list[str]) -> Block | None:
        for b in self.blocks:
            if b.kind == "heading":
                for p in patterns:
                    if re.search(p, b.text, re.IGNORECASE):
                        return b
        return None

    def has_text(self, patterns: list[str]) -> list[str]:
        return [p for p in patterns if re.search(p, self.text, re.IGNORECASE)]

    def tables(self) -> list[Block]:
        return [b for b in self.blocks if b.kind == "table"]


def split_sections(blocks: list[Block], schema: dict) -> dict[str, Section]:
    all_sec_defs: list[tuple[str, str, str, list[str]]] = []
    for sec_def in schema.get("sections", []):
        all_sec_defs.append(
            ("ch", sec_def["id"], sec_def["name"], sec_def.get("heading_patterns", []))
        )
    for app in schema.get("appendices", []):
        pat = rf"附录\s*{app['id']}|Appendix\s+{app['id']}"
        all_sec_defs.append(("app", f"appendix_{app['id']}", app["name"], [pat]))

    sec_starts: dict[str, int] = {}
    for _kind, sid, _name, patterns in all_sec_defs:
        for i, b in enumerate(blocks):
            if b.kind != "heading":
                continue
            for p in patterns:
                if re.search(p, b.text, re.IGNORECASE):
                    sec_starts[sid] = i
                    break
            else:
                continue
            break

    ordered = sorted(sec_starts.items(), key=lambda kv: kv[1])
    result: dict[str, Section] = {}
    for idx, (sid, start) in enumerate(ordered):
        end = ordered[idx + 1][1] if idx + 1 < len(ordered) else len(blocks)
        name = next((n for _, s, n, _ in all_sec_defs if s == sid), sid)
        result[sid] = Section(
            id=sid, name=name, start_idx=start, end_idx=end, blocks=blocks[start:end]
        )

    first_main_start = ordered[0][1] if ordered else len(blocks)
    result["_front_matter"] = Section(
        id="_front_matter",
        name="Front Matter",
        start_idx=0,
        end_idx=first_main_start,
        blocks=blocks[:first_main_start],
    )
    return result


